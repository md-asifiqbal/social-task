Route::middleware('auth:sanctum')->get('/user', function (Request $request) {
    return $request->user();
});

Route::controller(AuthController::class)->group(function () {
    //User Registration route
    Route::post('/auth/register', 'register');
    //User Login route
    Route::post('/auth/login', 'login');
});
